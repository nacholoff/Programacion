public class Array1 {
    public static void main(String[] args) {

        int numeros[] = new int[100];

        System.out.println(numeros[5]);

        for (int i = 0; i < numeros.length; i++) {
            numeros[i] = i + 1;
            System.out.println(numeros[i]);
        }

        //CREAR ARRAY Y MOSTRAR NUMEROS

    }
}
